# Print the name of the operating system
uname -o

# Print the login name
whoami

# Print the host name
hostname

# Display the calendar for January 2024
cal 1 2024

# Display the calendar for February 1995
cal 2 1995

# Display the calendar for the current month
cal

# Display the current date with day abbreviation, month abbreviation, and year
date +"%a %b %Y"

# Display the time in 12-hour format
date +"%I:%M %p"

# Display the time in 24-hour format
date +"%H:%M"

# Display the current date and time
date

# Display "GOOD MORNING" in enlarged characters (requires figlet)
figlet "GOOD MORNING"


